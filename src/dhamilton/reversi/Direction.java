package dhamilton.reversi;

/**
 * Directions for a othoganal movement on a square drid
 * 
 * @version 20/04/2021
 * @author Dylan Hamilton
 */
public enum Direction {
	/**
	 * Up Left (North West) direction
	 */
	UPLEFT,
	/**
	 * Up (North) direction
	 */
	UP,
	/**
	 * Up Right (North East) direction
	 */
	UPRIGHT,
	/**
	 * Left (West) direction
	 */
	LEFT,
	/**
	 * Right (East) direction
	 */
	RIGHT,
	/**
	 * Down Left (South West) direction
	 */
	DOWNLEFT,
	/**
	 * Down (South) direction
	 */
	DOWN,
	/**
	 * Down Right (South East) direction
	 */
	DOWNRIGHT
}
